	太阳神三国杀·幻想天使AI体系（处理模块·任务紧迫度分析文件）
	思想：将游戏中的事件归类整理，并从中总结出当前最紧迫的任务，所有行动都应尽可能地围绕这个任务展开。
		例如，在进攻方面，攻击A、攻击B、攻击C，是三个紧迫程度递减的任务，能同时攻击A、B、C时，应先攻击A。
		如果不能攻击或不能有效攻击A，则退而求其次，选择B进行攻击。
		又如，在防御方面，保护A、保护B，是两个紧迫程度递减的任务，
		如果一次南蛮入侵可能造成A、B死亡，应先考虑对A使用无懈可击。
		更一般地，对游戏局势的控制，有时侧重于控体力，有时侧重于控卡牌，有时又侧重于控距离或其他状态，等等。
		这些侧重首先会在任务紧迫度中得到直接的体现。
	意义：引入任务概念，可以增强同一阵营各角色的协作能力，同时也可以突出团队核心的领导能力，尽可能地凝聚力量。
		对于AI系统，任务分析应当可以看作一种自我学习的过程，而这也对出牌策略有着更为深刻的影响。
		通俗地讲就是，如果玩家希望电脑帮忙参与集火的话，自己多对着目标攻击几次就可以了，电脑会感受到玩家的意愿的。
